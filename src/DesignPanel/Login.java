    private void forgotpasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_forgotpasswordMouseClicked
        // TODO add your handling code here:
        
        
        String username = JOptionPane.showInputDialog(null, "Enter username");
        
        if (username == null) {
            // User pressed Cancel, so exit the method
            return;
        }
        
        // Check if username is not entered
        if (username.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter username", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String password = AMS.forgetPassword(username);
            if (password != null) {
                AMS.showErrorMessage("Your password is " + password);
            } else {
                AMS.showErrorMessage("Username not found");
            }
        }
        
        
    }//GEN-LAST:event_forgotpasswordMouseClicked

