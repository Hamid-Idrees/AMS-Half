    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        // TODO add your handling code here:
        
        if (!login_username.getText().isEmpty() && !new String(login_password.getPassword()).isEmpty()) {
            
            boolean isValid = AMS.verifyTeacher(login_username.getText(), new String(login_password.getPassword()));
            
            if (isValid) {
                this.setVisible(false);
                this.dispose();
                new Dashboard().setVisible(true);  // Assuming you have a Dashboard class
                
            } else {
                AMS.showErrorMessage("Username or Password is invalid");
            }
        } else {
            AMS.showErrorMessage("Fill the required fields");
        }
        
        
    }//GEN-LAST:event_loginActionPerformed

